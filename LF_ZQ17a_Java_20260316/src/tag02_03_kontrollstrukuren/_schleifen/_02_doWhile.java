package tag02_03_kontrollstrukuren._schleifen;

public class _02_doWhile {

	public static void main(String[] args) {
		
		//Do-While ist eine fußgesteuerte Schleife
		
		int zaehler = 1;
		
		do {
			System.out.println("Aktueller Zähler: " + zaehler);
			zaehler++;
		} while (zaehler <= 5);

	}

}
